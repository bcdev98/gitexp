def my_square(x):
    """Take a value and returns the squared value

    uses the ** operator
    """
    return (x ** 2)


def my_square2(x):
    """uses the * operator to calculate square
    to enhance the calculation
    by x * x
    """
    return (x * x)

def my_cube(x):
    return (x ** 3)

def my_cube3(x):
    return (x * x * x)


print(my_square(42))
print(my_square2(12))
print(my_square(10))
